void sort(int a[],int l,int u)
{
    int pivot=a[u-1],pos=l-1,i,t;
    if(l>=u)
        return;

    for(i=l;i<u-1;i++)
    {
        if(a[i]<=pivot)
        {
            t=a[pos+1];
            a[pos+1]=a[i];
            a[i]=t;
            pos+=1;
        }
    }
    t=a[pos+1];
    a[pos+1]=a[i];
    a[i]=t;
    pos+=1;
    sort(a,l,pos);
    sort(a,pos+1,u);
}
